/*
 *	A linguagem javascript é considerada de tipagem fraca
 *	porque os dados podem mudar de tipo facilmente, sem a
 *	necessidade de declarar qual o tipo de cada dado.
 */

//	Declarando e exibindo a variável 'qualquer'
let qualquer = 'Legal'
console.log(qualquer)
console.log(typeof qualquer)
//	Exibindo o tipo da variavel 'qualquer'

//	Atribuindo um novo valor e exibindo a variável 'qualquer'
qualquer = 3.1516
console.log(qualquer)
console.log(typeof qualquer)
//	Exibindo o tipo da variavel 'qualquer'

